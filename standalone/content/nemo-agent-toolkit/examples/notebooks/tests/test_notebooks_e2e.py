def _is_installed(package_name: str) -> bool:
    """Check if a package is installed."""
    try:
        subprocess.run(
            ["uv", "pip", "show", "-q", package_name],
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
            check=True,
        )
        return True
    except subprocess.CalledProcessError:
        return False


def _delete_workflow(notebooks_dir: Path, workflow_name: str, check: bool = True):
    try:
        subprocess.run(
            ["nat", "workflow", "delete", "--yes", workflow_name],
            check=check,
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
        )
    except subprocess.CalledProcessError:
        subprocess.run(["rm", "-rf", f"{notebooks_dir}/{workflow_name}"],
                       check=False,
                       stdout=subprocess.DEVNULL,
                       stderr=subprocess.DEVNULL)


def _delete_all_workflows(notebooks_dir: Path):
    for workflow in _ALL_WORKFLOWS:
        _delete_workflow(notebooks_dir, workflow, check=False)


def _delete_other_files(notebooks_dir: Path):
    for file in _OTHER_FILES:
        file_path = notebooks_dir / file
        if file_path.exists():
            file_path.unlink()


def _cleanup_all(notebooks_dir: Path):
    _delete_all_workflows(notebooks_dir)
    _delete_other_files(notebooks_dir)


